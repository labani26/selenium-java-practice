package Pack1;

public class SwitchCaseExample2 {

    public static void main(String[] args) {
        
  String browser = "operamini";
	 
	 switch (browser) {
	  
	  case "firefox" :
		  System.out.println("open firefox");
		  break;
	  case "operamini" :
		  System.out.println("open operamini");
		  
	  case "edge" :
		  System.out.println("open edge");
		  break;
	 default :
		 System.out.println("open nothing");
		 
	 }
	 
    }
	  
  }
